# Core Constraints（核心约束 — 三大定律）
# ============================================
CORE_CONSTRAINTS = """【🔴 创作铁律 — 不可违反】

| 定律 | 规则 | 检查方式 |
|------|------|----------|
| 大纲即法律 | 严格执行大纲，不得擅自发挥 | 审查时对照大纲 |
| 设定即物理 | 实力/招式/物品 ≤ 已有设定 | 写作前确认 |
| 发明需识别 | 新角色/地点/物品正常描写，后续自动追踪 | 完成后检查 |

Hard（必须）：
- 读者能回答"发生了什么/谁在做什么/为什么"
- 本章有清晰推进：问题/目标/代价/关系变化/信息变化 至少一项
- 若上章有明确承诺（钩子/未闭合问题），本章必须回应
- 禁止占位正文（[待补充]/[TODO]/...省略...）

Soft（建议）：
- 开头尽早进入冲突/风险/强情绪（前200-400字）
- 章末保留未闭合问题或下一章期待锚点
- 局面变化保持节奏感（800-1400字一个脉冲）
- 对话带意图（试探/回避/施压/诱导），减少纯说明句"""


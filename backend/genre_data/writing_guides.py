ANTI_AI_GUIDE = """【🔴 Anti-AI 写作规范 — 必须遵守】

作为 LLM，你有以下根深蒂固的倾向，写作时必须刻意对抗：

1. ❌ 每段写完整闭环 → ✅ 刻意删掉感悟，留未解决的余味
2. ❌ 用副词修饰一切（缓缓/淡淡/微微/轻轻）→ ✅ 删掉副词，用具体动作替代
3. ❌ 所有角色同一套反应（瞳孔微缩/心中一凛）→ ✅ 给每个角色设计专属微动作
4. ❌ 对话像辩论赛（完整理性表达）→ ✅ 让角色说话带潜台词，言行不一，有省略和打断
5. ❌ 情绪贴标签（他感到愤怒）→ ✅ 改为生理反应+微动作，让读者自己判断
6. ❌ 信息均匀分布 → ✅ 刻意制造疏密对比，有的段落只有一句话
7. ❌ 安全着陆（章末解决所有冲突）→ ✅ 刻意留下至少一个未解决的问题
8. ❌ 展示后解释（做完动作再解释一遍）→ ✅ 删掉解释句，信任读者的理解力

替代方案速查：
- "他感到愤怒" → "指节捏得发白，舌尖尝到铁锈味。再等一秒他就动手。"
- "缓缓说道" → "他把杯子搁下——'你确定？'"
- "眸中闪过一丝X" → "她咬了下嘴唇内侧"（紧张）/ "他开始拧笔帽"（焦虑）
- "心中暗道" → 直接删掉这四个字，后面内容保留
- "更重要的是……" → 直接说事，删掉递进模板词

对话规范：
- 有人抢话、有人沉默、有人答非所问
- 不同角色有不同说话节奏（有人啰嗦、有人惜字如金）
- said tag 不超过30%，优先用前置动作"""

